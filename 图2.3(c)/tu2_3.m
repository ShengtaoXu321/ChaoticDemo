clear;clc
a=10;b=0.545;
for m=1:801
    x(1)=-0.2+0.001*(m-1);
    for n=1:4
        x(n+1)=b*exp(-a*x(n)*x(n))-b*x(n)*x(n);
    end
    x1(m)=-0.2+0.001*(m-1);
    y1(m)=x(end);
end
plot(x1,y1,x1,x1);