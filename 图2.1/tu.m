clear;clc
a=10;
x=-1:0.001:1;
f1=1*(exp(-a*x.*x)-x.*x);
f2=0.7*(exp(-a*x.*x)-x.*x);
f3=0.4*(exp(-a*x.*x)-x.*x);
f4=-0.4*(exp(-a*x.*x)-x.*x);
f5=-0.7*(exp(-a*x.*x)-x.*x);
f6=-1*(exp(-a*x.*x)-x.*x);
f7=x;
plot(x,f1,x,f2,x,f3,x,f4,x,f5,x,f6,x,f7)